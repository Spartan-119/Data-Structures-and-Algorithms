class BinaryTreeNode:
    def __init__(self, data):
        self.data = data
        self.left = None
        self.right = None

        # setter
        def set_data(self, data):
            self.data = data

        # getter
        def get_data(self):
            return self.data

        # get the left node
        def get_left(self):
            return self.left

        # get the right node
        def get_right(self):
            return self.right